import UIKit

extension UIFont {
    static func truenoSemiBold(_ size: CGFloat) -> UIFont {
        UIFont(name: "TruenoSBd", size: size)!
    }

    static func truenoLight(_ size: CGFloat) -> UIFont {
        UIFont(name: "TruenoLt", size: size)!
    }

    static func truenoRegular(_ size: CGFloat) -> UIFont {
        UIFont(name: "TruenoRg", size: size)!
    }

    static func truenoExtraBold(_ size: CGFloat) -> UIFont {
        UIFont(name: "TruenoExBd", size: size)!
    }
}
